# --------------- Operadores Relacionais --------------- #

# Atribuindo variáveis
a = 7 # a recebe 7 ----- Operador de Atribuição
b = 5 # b recebe 5

# Operadores
a > 8 # Maior que
a < 8 # Menor que
a <= 8 # Menor igual que
a >= 8 # Maior igual que
a == 8 # Igual
a != 8 # Diferente

# Operadores lógicos
(a == 8) & (b == 6) # Operador lógico and 
(a == 8) | (b > 5) # Operador lógico or

a > 8 
# "!" Operador not
print(! a > 8) # "!" esse operador contradiz Ex.: Em uma senteça verdadeira ele
               # contradiz dizendo que é falso --- Apensar de a não ser verdairo
               # estou retornando verdadeiro















